package com.publicis.sapient.p2p.vo;
/**
 * AuditReqResponse
 * 
 *@author Sapient
 * 
 */
public class AuditReqResponse {
    /**
     * property to hold requestData
     */
    private String mRequestData;
    /**
     * property to hold responseData
     */
    private String mResponseData;
    /**
     * @return the requestData
     */
    public String getRequestData() {
        return mRequestData;
    }
    /**
     * @param pRequestData the requestData to set
     */
    public void setRequestData(String pRequestData) {
        mRequestData = pRequestData;
    }
    /**
     * @return the responseData
     */
    public String getResponseData() {
        return mResponseData;
    }
    /**
     * @param pResponseData the responseData to set
     */
    public void setResponseData(String pResponseData) {
        mResponseData = pResponseData;
    }
    
    
    
}
